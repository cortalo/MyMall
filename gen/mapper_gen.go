// gen/mapper_gen.go
package main

import (
	"flag"
	"go/ast"
	"go/parser"
	"go/token"
	"os"
	"path/filepath"
	"strings"
	"text/template"
)

var mapperTemplate = `// Code generated by mapper_gen.go; DO NOT EDIT.
package {{.Package}}

import "{{.DomainPkg}}"

func toDomain(po *{{.SrcType}}) *{{.DstType}} {
	return &{{.DstType}}{
		{{- range .Fields}}
		{{.}}: po.{{.}},
		{{- end}}
	}
}

func toPo(region *{{.DstType}}) *{{.SrcType}} {
	return &{{.SrcType}}{
		{{- range .Fields}}
		{{.}}: region.{{.}},
		{{- end}}
	}
}
`

type TemplateData struct {
	Package   string
	DomainPkg string
	SrcType   string
	DstType   string
	Fields    []string
}

func main() {
	src := flag.String("src", "", "source struct name, e.g. RegionPo")
	dst := flag.String("dst", "", "destination type, e.g. domain.Region")
	pkg := flag.String("pkg", "", "domain package import path")
	out := flag.String("out", "", "output file name")
	flag.Parse()

	// 读取当前目录下所有 .go 文件，找到 src struct 的字段
	fields := parseStructFields(".", *src)

	// 生成代码
	data := TemplateData{
		Package:   "infra",
		DomainPkg: *pkg,
		SrcType:   *src,
		DstType:   *dst,
		Fields:    fields,
	}

	outPath := filepath.Join(".", *out)
	f, _ := os.Create(outPath)
	defer func() {
		if err := f.Close(); err != nil {
			panic(err)
		}
	}()

	tmpl, err := template.New("mapper").Parse(mapperTemplate)
	if err != nil {
		panic(err)
	}
	if err := tmpl.Execute(f, data); err != nil {
		panic(err)
	}
}

func parseStructFields(dir string, structName string) []string {
	var fields []string

	entries, err := os.ReadDir(dir)
	if err != nil {
		panic(err)
	}

	fileSet := token.NewFileSet()
	for _, entry := range entries {
		if entry.IsDir() || !strings.HasSuffix(entry.Name(), ".go") {
			continue
		}
		file, err := parser.ParseFile(fileSet, filepath.Join(dir, entry.Name()), nil, 0)
		if err != nil {
			panic(err)
		}
		ast.Inspect(file, func(n ast.Node) bool {
			typeSpec, ok := n.(*ast.TypeSpec)
			if !ok || typeSpec.Name.Name != structName {
				return true
			}
			structType, ok := typeSpec.Type.(*ast.StructType)
			if !ok {
				return true
			}
			for _, field := range structType.Fields.List {
				if len(field.Names) > 0 {
					fields = append(fields, field.Names[0].Name)
				}
			}
			return true
		})
	}
	return fields
}
